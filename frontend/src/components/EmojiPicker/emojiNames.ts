const normalizeEmojiLocale = (locale: string | undefined): EmojiLocale => {
  const baseLocale = (locale ?? 'en').toLowerCase().split('-')[0];
  if (baseLocale === 'de' || baseLocale === 'fr' || baseLocale === 'hu' || baseLocale === 'es') {
    return baseLocale;
  }
  return 'en';
};

export const resolveEmojiLocalizedName = (
  emojiId: string,
  locale: string | undefined,
  fallbackName: string,
): string => {
  const normalizedLocale = normalizeEmojiLocale(locale);
  return EMOJI_LOCALIZED_NAMES[normalizedLocale][emojiId]
    ?? EMOJI_LOCALIZED_NAMES.en[emojiId]
    ?? fallbackName;
};
